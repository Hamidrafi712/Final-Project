    public void initTablePelanggan() {
        //Deklarasi array bookColumns tipe data String
        String[] pelangganColumns = new String[]{"Id Pelanggan", "Nama", "No.Telp", "Alamat"};
        
        int[] columnWidth = {//Deklarasi array columWidth tipe data int
            135, 135, 135, 135 //lebar tiap kolom
        };
        
        tableModel = new DefaultTableModel(pelangganColumns, 0);//Deklarasi tableModel 
        pelangganTabel.setModel(tableModel);//Set table model
        pelangganTabel.setRowHeight(20);//set jumlah baris table
        
        int i = 0;
        for (int width : columnWidth) {
            TableColumn column = pelangganTabel.getColumnModel().getColumn(i++);//set column bookTable
            column.setMaxWidth(width);//set lebar table
            column.setPreferredWidth(width);//set lebar table                
        }       
    }
    
    public static void loadPelanggan(){ //Method untuk memuat data pelanggan
        listPelangganModel = new ArrayList<>(); //Deklarasi arrayList
        listPelangganModel = PelangganManager.semuaPelanggan(); //lalu nilainya adalah return nilai dari method semua pelanggan
        tableModel.setRowCount(0); //set unutk jumlah baris menjadi 0
        listPelangganModel.forEach(pelangganModel ->{ //Melakukan perulangan untuk memasukkan data pelanggan
                tableModel.addRow(new Object[]{
                    pelangganModel.getId_pelanggan(),
                    pelangganModel.getNama(),
                    pelangganModel.getNo_telp(),
                    pelangganModel.getAlamat()
                });
        });      
    }

