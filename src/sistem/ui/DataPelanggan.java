    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = pelangganTabel.getSelectedRow(); //fungsi untuk mengambil data 
        if (selectedRow == -1){
            showMessage("Pilih Baris dulu!", 1);
        }else{
           PelangganModel pelanggan = listPelangganModel.get(selectedRow);
           new TambahKonsumen("Ubah Konsumen", pelanggan.getId_pelanggan()).setVisible(true);
        }
        dispose();
    }//GEN-LAST:event_updateBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = pelangganTabel.getSelectedRow();
        if (selectedRow == -1){
            showMessage("Pilihlah Baris Terlebih Dahulu", 2);
        }else{
            int option = JOptionPane.showConfirmDialog(this, "Apakah anda yakin akan menghapus data ini?", "Hapus Data", JOptionPane.WARNING_MESSAGE);
            if (option == JOptionPane.YES_OPTION){
                try{
                    Connection conn = ConnectionHelper.getConnection(); //digunakan untuk mendapatkan komeksi dengan database
                    Statement stmn = conn.createStatement(); //digunakan untuk pengiriman statement SQL tanpa parameter, agar bisa melalukan query
                    PelangganModel pelanggan = listPelangganModel.get(selectedRow);
                    stmn.executeUpdate("DELETE FROM pelanggan WHERE id_pelanggan = " + pelanggan.getId_pelanggan());
                    loadPelanggan();
                }catch (Exception e){
                    System.out.println("ERROR" + e.getMessage());
                }
            }
        }   
    }//GEN-LAST:event_deleteBtnActionPerformed

