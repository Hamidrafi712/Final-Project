    public void initTableBarang() {
        //Deklarasi array bookColumns tipe data String
        String[] barangColumns = new String[]{"Id Barang", "Nama Barang", "Tanggal Exp", "Jumlah", "Harga", "Kategori"};
        
        int[] columnWidth = {//Deklarasi array columWidth tipe data int
            100, 100, 100, 100, 100, 100 //lebar tiap kolom
        };
        
        tableModel = new DefaultTableModel(barangColumns, 0);//Deklarasi tableModel 
        barangTabel.setModel(tableModel);//Set table model
        barangTabel.setRowHeight(20);//set jumlah baris table
        
        int i = 0;
        for (int width : columnWidth) {
            TableColumn column = barangTabel.getColumnModel().getColumn(i++);//set column bookTable
            column.setMaxWidth(width);//set lebar table
            column.setPreferredWidth(width);//set lebar table                
        }       
        validateExite();
    }
    
