    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = barangTabel.getSelectedRow(); //fungsi untuk mengambil data 
        if (selectedRow == -1){
            showMessage("Pilih Baris dulu!", 1);
        }else{
           BarangModel pelanggan = listBarangModel.get(selectedRow);
           new TambahProduk("Ubah Data Produk", pelanggan.getId_barang()).setVisible(true);
        }
        dispose();
    }//GEN-LAST:event_updateBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = barangTabel.getSelectedRow();
        if (selectedRow == -1){
            showMessage("Pilihlah Baris Terlebih Dahulu", 2);
        }else{
            int option = JOptionPane.showConfirmDialog(this, "Apakah anda yakin akan menghapus data ini?", "Hapus Data", JOptionPane.WARNING_MESSAGE);
            if (option == JOptionPane.YES_OPTION){
                try{
                    Connection conn = ConnectionHelper.getConnection(); //digunakan untuk mendapatkan komeksi dengan database
                    Statement stmn = conn.createStatement(); //digunakan untuk pengiriman statement SQL tanpa parameter, agar bisa melalukan query
                    BarangModel barang = listBarangModel.get(selectedRow);
                    stmn.executeUpdate("DELETE FROM barang WHERE id_barang = " + barang.getId_barang());
                    loadBarang("");
                }catch (Exception e){
                    System.out.println("ERROR" + e.getMessage());
                }
            }
        }   
    }//GEN-LAST:event_deleteBtnActionPerformed

